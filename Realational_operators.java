package Madhura;

public class Realational_operators {
	public static void main(String[] args) {
	int a = 9, b =  10;
	
	System.out.println("a is "+a+"and b is "+b);
	
	System.out.println(a == b);
	
	System.out.println(a != b);
	
	System.out.println(a>b);
	
	System.out.println(a<b);
	
	System.out.println(a >= b);
	
	System.out.println(a <= b);
	}

}
